// Map categories to appropriate icons
export const getCategoryIcon = (category, props = {}) => {
  const cat = category.toLowerCase();
  if (cat.includes('linux')) return <LinuxIcon {...props} />;
  if (cat.includes('git')) return <GitIcon {...props} />;
  if (cat.includes('docker')) return <DockerIcon {...props} />;
  if (cat.includes('kubernetes') || cat.includes('k8s')) return <KubernetesIcon {...props} />;
  if (cat.includes('terraform')) return <TerraformIcon {...props} />;
  if (cat.includes('aws')) return <AwsIcon {...props} />;
  if (cat.includes('jenkins')) return <JenkinsIcon {...props} />;
  return <TerminalIcon {...props} />;
};
